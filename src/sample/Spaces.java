package sample;

public class Spaces
{
    private int x = 0, y = 0;

    public Spaces(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return x;
    }

    public void setX(int num)
    {
        x = num;
    }

    public int getY()
    {
        return y;
    }

    public void setY(int num)
    {
        y = num;
    }
}