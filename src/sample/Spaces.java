package sample;

public class Spaces
{
    int xLoc,yLoc;

    public Spaces(int x, int y)
    {
        xLoc = x;
        yLoc = y;
    }

    public int getX()
    {
        return xLoc;
    }

    public void setX(int xLoc)
    {
        this.xLoc = xLoc;
    }

    public int getY()
    {
        return yLoc;
    }

    public void setY(int yLoc)
    {
        this.yLoc = yLoc;
    }
}